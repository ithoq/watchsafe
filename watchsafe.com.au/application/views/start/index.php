        <h3>It all started as an electronics project.</h3>
        <p>Founder and Director Matthew Gleeson has always been interested in electronics and motor vehicles. He was working on a project to extract engine data from his car when he thought how useful it would be if the data could be sent over the mobile network.</p>
        <p>From here the project took another turn. He thought that if the engine data could be combined with GPS and other telemetry information it would be possible to gain insights into how the vehicle is being driven.</p>
        <p>Matthew was discussing the project and its benefits for young road users with his longtime friend Kash Sharma when they thought there isnt really anything on the market thats suited to families and small business.</p>
